export class ListeUser {

  nom:string
  prenom:string
  numero:string
  email:string
  adresse:string
  gouvernorat:string

constructor(nom:string,
  prenom:string,
  numero:string,
  email:string,
  adresse:string,
  gouvernorat:string){
      this.nom=nom
      this.prenom=prenom
      this.numero=numero
      this.email=email
      this.adresse=adresse
      this.gouvernorat=gouvernorat
  }

}
